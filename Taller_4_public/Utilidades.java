package Taller_4_public;

public class Utilidades {
    
    public static void suma(int numeroA, int numeroB){
        System.out.println(numeroA + " + " + numeroB + " = " + (numeroA+numeroB));
    }

    public static void resta(int numeroA, int numeroB){
        System.out.println(numeroA + " - " + numeroB + " = " + (numeroA-numeroB));
    }

    public static void multiplicacion(int numeroA, int numeroB){
        System.out.println(numeroA + " x " + numeroB + " = " + (numeroA*numeroB));
    }

    public static void divicion(int numeroA, int numeroB){
        System.out.println(numeroA + " / " + numeroB + " = " + (numeroA/numeroB));
    }

}
